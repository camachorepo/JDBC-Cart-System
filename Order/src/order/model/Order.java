package order.model;

public class Order {

	
private int id;  
private int item_id; 
private int Quantity;
public int getId() {
	return id;
}
public void setId(int id) {
	this.id = id;
}
public int getItem_id() {
	return item_id;
}
public void setItem_id(int item_id) {
	this.item_id = item_id;
}
public int getQuantity() {
	return Quantity;
}
public void setQuantity(int quantity) {
	Quantity = quantity;
}


	

}
